// BlogPostCard Component
const BlogPostCard = ({ title, image, excerpt, date }) => {
  const [isExpanded, setIsExpanded] = useState(false);

  const toggleExpand = () => {
    setIsExpanded(!isExpanded);
  };

  return (
    <div className="bg-gray-900 rounded-xl shadow-xl overflow-hidden border border-gray-800 transform transition-transform duration-300 hover:scale-105 hover:shadow-2xl">
      <img
        src={image}
        alt={title}
        className="w-full h-64 object-cover object-center border-b border-gray-800"
        onError={(e) => { e.target.onerror = null; e.target.src="https://placehold.co/600x400/222222/cccccc?text=Image+Not+Found"; }}
      />
      <div className="p-6">
        <h4 className="text-3xl font-playfair-display font-bold text-white mb-3 leading-snug">
          {title}
        </h4>
        <p className="text-gray-400 text-sm mb-4">
          Published on {date}
        </p>
        <p className="text-gray-300 text-base mb-4">
          {isExpanded ? excerpt : `${excerpt.substring(0, 150)}...`}
        </p>
        <button
          onClick={toggleExpand}
          className="text-purple-400 hover:text-purple-300 font-semibold text-sm transition duration-300"
        >
          {isExpanded ? 'Read Less' : 'Read More'}
        </button>
      </div>
    </div>
  );
};

export default App;
